<h2>Installation</h2>

<p>First, you need to input your Answer Customers 
api key. You can get your api key from your account page 
at <a href="https://answercustomers.com/account">https://answercustomers.com/account</a></p>

<p>If you don't have an account, you casn sign up at <a href="https://answercustomers.com/">https://answercustomers.com/</a>.</p>

<p>After you input your api key, you need to select the author and the category you want
to post as. Any content you send through Answer Customers will be posted as a draft
using the selected author and category.</p>

<p>Now save your settings and your WordPress should be ready to go.</p>

<p>To finish installation, you will need to make sure your Answer Customers account is 
pointed to your blog url in the Answer Customers account settings and you will need to 
install the Zendesk plugin inside of Zendesk.</p>

<p>Your blog url is the main url of your blog installation. So, if your site is 
<a href="http://mysite.com/">http://mysite.com/</a> then your blog url is going to be <a href="http://mysite.com/">http://mysite.com/</a> If your WordPress
is installed on a subdirectory, then it is going to be that subdirectory, like say, 
<a href="http://mysite.com/blog/">http://mysite.com/blog/</a></p>

<h2>Usage</h2>

<p>To send Zendesk tickets to WordPress, you just press the "Send To Answer Customers" button on
the support ticket inside of Zendesk. The content will be sent to your WordPress blog
and saved as a draft. </p>

<p>Once the content is inside of your blog, you need to edit it down to be more presentable
to your website visitors. We recommend removing any of your customer's personal information
and editing for clarity, so that you can provide the most value to all of your future
customers who read the article later.</p>

<p>It is also a good idea to use a SEO plugin to make sure that your on page SEO for 
your articles is helping you get more search engine traffic. The Yoast SEO plugin does
a pretty good job of this.</p>

<p>The last thing to get the most value out of your content is to have a strong call to action
after each article. Have a lead generation form, a newsletter signup, a free ebook
download, or even a short call to action with your business phone number right there. If
you need help with that, let us know at support@answercustomers.com</p>

